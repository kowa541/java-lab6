/**
 * <p>Аннотация, используемая для указания кешируемых областей данных в классе.</p>
 *
 * <p>Применяется исключительно к объявлениям типов (классам). Предназначена для
 * маркировки классов, чьи экземпляры или поля могут быть кандидатами для кеширования.</p>
 *
 * <p>Содержит необязательный параметр {@code value}, представляющий массив строк,
 * который может использоваться для указания конкретных областей или ключей кеширования.</p>
 *
 * <h3>Пример использования:</h3>
 * <pre>{@code
 * @Cache({"userProfile", "settings"})
 * public class UserData { ... }
 * }</pre>
 *
 * @see java.lang.annotation.Target
 * @see java.lang.annotation.Retention
 */
@Target(ElementItType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
public @interface Cache {
    /**
     * Определяет массив строк, идентифицирующих кешируемые области.
     * По умолчанию — пустой массив.
     *
     * @return массив строк с именами кешируемых областей
     */
    String[] value() default {};
}
