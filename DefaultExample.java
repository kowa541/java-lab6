/**
 * <p>Пример класса, демонстрирующего использование аннотации {@link Default}.</p>
 *
 * <p>Класс помечен аннотацией {@code @Default(String.class)}, что может указывать,
 * например, на то, что при обработке этого типа по умолчанию следует использовать
 * {@link String} в качестве резервного или контекстного типа (в зависимости от логики фреймворка).</p>
 *
 * <p>Содержит публичное поле {@code name} со значением по умолчанию, показывающее
 * базовую структуру данных, связанную с этим примером.</p>
 *
 * <h3>Пример объявления:</h3>
 * <pre>{@code
 * @Default(String.class)
 * public class DefaultExample {
 *     public String name = "Test";
 * }
 * }</pre>
 *
 * <p>Этот класс предназначен исключительно для демонстрационных целей
 * и не содержит бизнес-логики.</p>
 *
 * @see Default
 */
@Default(String.class)
public class DefaultExample {
    /**
     * Пример публичного поля с инициализированным значением.
     * Может использоваться для тестирования или отладки.
     */
    public String name = "Test";
}
