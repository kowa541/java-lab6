/**
 * <p>Пример класса, демонстрирующего использование аннотации {@link ToString}
 * как на уровне класса, так и на уровне отдельных полей.</p>
 *
 * <p>Класс аннотирован как {@code @ToString}, что указывает на желание включать
 * его поля в строковое представление по умолчанию. Однако поведение для конкретных полей
 * переопределяется с помощью аннотаций непосредственно над ними:</p>
 *
 * <ul>
 *   <li>Поле {@link #name} явно включено ({@code Mode.YES}) — будет отображаться;</li>
 *   <li>Поле {@link #secret} исключено ({@code Mode.NO}) — не должно входить в вывод;</li>
 *   <li>Поле {@link #score} явно включено ({@code Mode.YES}) — будет отображаться.</li>
 * </ul>
 *
 * <p>Этот класс может использоваться для тестирования или демонстрации логики,
 * которая динамически формирует строковое представление объекта на основе аннотаций.</p>
 *
 * <h3>Ожидаемое строковое представление (пример):</h3>
 * <pre>{@code
 * ToStringExample{name='Alice', score=95.5}
 * // Поле 'secret' исключено из вывода
 * }</pre>
 *
 * @see ToString
 * @see Mode
 */
@ToString
public class ToStringExample {

    /**
     * Имя объекта. Включается в строковое представление.
     */
    @ToString(Mode.YES)
    public String name = "Alice";

    /**
     * Конфиденциальное значение. Исключается из строкового представления.
     */
    @ToString(Mode.NO)
    public int secret = 42;

    /**
     * Числовой показатель (например, оценка). Включается в строковое представление.
     */
    @ToString(Mode.YES)
    public double score = 95.5;
}
