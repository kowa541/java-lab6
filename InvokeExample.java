/**
 * <p>Пример класса, содержащего метод, помеченный аннотацией {@link Invoke}.</p>
 *
 * <p>Этот класс демонстрирует, как может выглядеть компонент с автоматически вызываемым методом.
 * Метод {@link #doSomething()} помечен аннотацией {@code @Invoke}, что позволяет внешним фреймворкам
 * или тестам обнаруживать и вызывать его динамически через рефлексию.</p>
 *
 * <p>Класс также содержит вспомогательное поле {@link #wasCalled}, которое изменяется при вызове
 * аннотированного метода — это позволяет проверять, действительно ли метод был выполнен.</p>
 *
 * <h3>Пример использования:</h3>
 * <pre>{@code
 * InvokeExample example = new InvokeExample();
 * // ... обнаружение метода с @Invoke через рефлексию и вызов
 * assert example.wasCalled; // должно быть true после вызова
 * }</pre>
 *
 * @see Invoke
 */
public class InvokeExample {

    /**
     * Флаг, указывающий, был ли вызван метод, помеченный аннотацией {@link Invoke}.
     * Изначально имеет значение {@code false}, изменяется на {@code true} при вызове
     * метода {@link #doSomething()}.
     */
    public boolean wasCalled = false;

    /**
     * Метод, автоматически вызываемый при обнаружении аннотации {@link Invoke}.
     *
     * <p>Выводит сообщение в консоль и устанавливает флаг {@link #wasCalled} в {@code true},
     * сигнализируя о том, что метод был исполнен.</p>
     */
    @Invoke
    public void doSomething() {
        System.out.println("   [!] Метод с @Invoke был вызван!");
        this.wasCalled = true;
    }

    /**
     * Вспомогательный метод без аннотации, используемый для демонстрации
     * различия между аннотированными и неаннотированными методами.
     *
     * <p>Этот метод не должен обнаруживаться системой, ищущей {@code @Invoke}.</p>
     */
    public void notAnnotated() {
        System.out.println("Этот метод НЕ аннотирован.");
    }
}
