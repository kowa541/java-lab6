
/**
 * <p>Перечисление, определяющее режим отображения поля в строковом представлении объекта.</p>
 *
 * <p>Используется для управления тем, должно ли конкретное поле быть включено
 * в результат метода {@code toString()} или аналогичного строкового представления.</p>
 *
 * <p>Возможные значения:
 * <ul>
 *   <li>{@link #YES} — поле должно быть включено в строковое представление;</li>
 *   <li>{@link #NO}  — поле должно быть исключено из строкового представления.</li>
 * </ul>
 * </p>
 *
 * <p>Это перечисление может применяться, например, в пользовательских аннотациях
 * или утилитах сериализации/логирования для гибкой настройки вывода.</p>
 */
public enum Mode {
    /**
     * Указывает, что поле должно быть включено в строковое представление.
     */
    YES,

    /**
     * Указывает, что поле должно быть исключено из строкового представления.
     */
    NO
}
