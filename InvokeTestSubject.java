/**
 * <p>Тестовый класс, предназначенный для проверки поведения аннотации {@link Invoke}.</p>
 *
 * <p>Содержит метод {@link #doSomething()}, помеченный аннотацией {@code @Invoke},
 * который изменяет внутреннее состояние объекта при вызове. Класс также предоставляет
 * метод доступа {@link #wasInvoked()} для проверки этого состояния.</p>
 *
 * <p>Используется в модульных тестах для подтверждения, что фреймворк или утилита
 * корректно обнаруживает и вызывает аннотированные методы через рефлексию.</p>
 *
 * <p>Дополнительно содержит метод {@link #doNothing()}, не помеченный аннотацией,
 * чтобы продемонстрировать, что только методы с {@code @Invoke} должны обрабатываться.</p>
 *
 * @see Invoke
 */
public class InvokeTestSubject {

    /**
     * Внутренний флаг, отслеживающий, был ли вызван метод с аннотацией {@link Invoke}.
     * Изначально {@code false}; устанавливается в {@code true} при вызове {@link #doSomething()}.
     */
    private boolean wasInvoked = false;

    /**
     * Метод, автоматически вызываемый при обнаружении аннотации {@link Invoke}.
     *
     * <p>Устанавливает внутренний флаг {@link #wasInvoked} в значение {@code true},
     * что позволяет внешним тестам или фреймворкам проверить факт его выполнения.</p>
     */
    @Invoke
    public void doSomething() {
        this.wasInvoked = true;
    }

    /**
     * Возвращает текущее значение флага, указывающего, был ли вызван аннотированный метод.
     *
     * @return {@code true}, если метод {@link #doSomething()} был вызван, иначе {@code false}
     */
    public boolean wasInvoked() {
        return this.wasInvoked;
    }

    /**
     * Вспомогательный метод без аннотации, предназначенный для контраста.
     *
     * <p>Сбрасывает флаг {@link #wasInvoked} в {@code false}.
     * Не должен обнаруживаться системой, ищущей методы с аннотацией {@code @Invoke}.</p>
     */
    public void doNothing() {
        this.wasInvoked = false;
    }
}
