
/**
 * <p>Аннотация, содержащая два обязательных параметра: строку и целое число.</p>
 *
 * <p>Предназначена для применения исключительно к объявлениям типов
 * ({@code ElementType.TYPE}) и сохраняется во время выполнения
 * ({@code RetentionPolicy.RUNTIME}), что позволяет использовать её
 * через рефлексию.</p>
 *
 * <p>Может применяться, например, для привязки метаданных к классу — таких как
 * идентификатор и человекочитаемое имя, версия и описание, или любая другая
 * пара «текст + число», требуемая в бизнес-логике или фреймворке.</p>
 *
 * <h3>Пример использования:</h3>
 * <pre>{@code
 * @Two(first = "user-profile", second = 1)
 * public class UserProfile { ... }
 * }</pre>
 *
 * @see java.lang.annotation.Target
 * @see java.lang.annotation.Retention
 */
@Target(ElementType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
public @interface Two {

    /**
     * Возвращает строковое значение, связанное с аннотированным типом.
     * Это обязательное свойство аннотации.
     *
     * @return строка, описывающая или идентифицирующая тип
     */
    String first();

    /**
     * Возвращает целочисленное значение, связанное с аннотированным типом.
     * Это обязательное свойство аннотации.
     *
     * @return целое число (например, версия, приоритет, идентификатор)
     */
    int second();
}
